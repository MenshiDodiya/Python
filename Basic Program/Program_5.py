#take 2 inputs from the user and perform addition, substraction, multiplication, divison and modulo

num1 = int(input("Enter the number 1:- "))
num2 = int(input("Enter the number 2:- "))
print (f"The addition of the {num1} + {num2} is {num1+num2}")
print (f"The substraction of the {num1} - {num2} is {num1-num2}")
print (f"The multiplication of the {num1} * {num2} is {num1*num2}")
print (f"The divison of the {num1} / {num2} is {num1/num2}")
print (f"The modulo of the {num1} % {num2} is {num1%num2}")